public class A4_Data_Types {
    public static void main(String[] arg) {

          //------------- boolean ----------------
        boolean areYouSingle = true;
        boolean doYouHaveGirlFriend = false;
        System.out.println(areYouSingle);
        System.out.println(doYouHaveGirlFriend);


        //-------------- characters ---------------
        char myGrade = 'A';
        System.out.println(myGrade);


        //---------- Alternatively you can use ASCII values
        char a=65, b=66, c=67;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);


        //------------ String ---------------------------
        String tutor = "Yash Jain";
        System.out.println(tutor);


    }
}